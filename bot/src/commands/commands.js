const path = require('node:path')
const fs = require('node:fs')
const start = require('./handlers/start')


class Commands {
	commands = [
		{
			command: 'start', action: (ctx) => {
				return ctx.reply(start(ctx))
			}
		}
	]
	
	constructor(bot) {
		this.bot = bot
	}
	
	// –∑–∞–≥—Ä—É–∑–∏—Ç—å –∫–æ–º–∞–Ω–¥—ã
	async loadCommands() {
		try {
			console.log('Load commands...')
			this.commands.forEach(({command, action}) => {
				console.log(`üëâ Commands: /${command}`)
				this.bot.command(command, async (ctx) => {
					await action(ctx)
				})
				console.log('‚úÖ Commands loaded...')
			})
			this.bot.hears(/^\/sub.*/, (ctx) => {
				ctx.command = 'subscribe'
				this.bot.commands.find(({command}) => command === 'subscribe').action(ctx)
			})
		} catch (err) {
			console.log('‚ùå Error loading commands...\n' + err.message)
		}
	}
}

module.exports = {
	Commands: Commands
}